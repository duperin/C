#include <stdio.h>

int main(int argc, const char * argv[])
{

    // Declarando variaveis tipo Float
    float numero1;
    float numero2;
    
    // Definindo valor para variaveis
    numero1 = 3.14;
    numero2 = 42.0;
    
    // Declarando variavel tipo Double
    double resultado;
    
    // Calcular soma dos numeros
    resultado = numero1 + numero2 ;
    
    //Imprimir resultado na tela
    printf("A soma dos números é: %f.\n", resultado) ;
    
    
    
}

